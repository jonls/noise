static float
dot3(const float a[], float x, float y, float z)
{
	return a[0]*x + a[1]*y + a[2]*z;
}

#define FASTFLOOR(x)  (((x) >= 0) ? (int)(x) : (int)(x)-1)

float __attribute__ ((pure))
simplex3d(float x, float y, float z)
{
	/* Skew input space */
	float s = (x+y+z)*(1.0/3.0);
	int i = FASTFLOOR(x+s);
	int j = FASTFLOOR(y+s);
	int k = FASTFLOOR(z+s);

	/* Unskew */
	float t = (float)(i+j+k)*(1.0/6.0);
	float gx0 = i-t;
	float gy0 = j-t;
	float gz0 = k-t;
	float x0 = x-gx0;
	float y0 = y-gy0;
	float z0 = z-gz0;

	/* Determine simplex */
	int i1, j1, k1;
	int i2, j2, k2;

	if (x0 >= y0) {
		if (y0 >= z0) {
			i1 = 1; j1 = 0; k1 = 0;
			i2 = 1; j2 = 1; k2 = 0;
		} else if (x0 >= z0) {
			i1 = 1; j1 = 0; k1 = 0;
			i2 = 1; j2 = 0; k2 = 1;
		} else {
			i1 = 0; j1 = 0; k1 = 1;
			i2 = 1; j2 = 0; k2 = 1;
		}
	} else {
		if (y0 < z0) {
			i1 = 0; j1 = 0; k1 = 1;
			i2 = 0; j2 = 1; k2 = 1;
		} else if (x0 < z0) {
			i1 = 0; j1 = 1; k1 = 0;
			i2 = 0; j2 = 1; k2 = 1;
		} else {
			i1 = 0; j1 = 1; k1 = 0;
			i2 = 1; j2 = 1; k2 = 0;
		}
	}

	/* Calculate offsets in x,y,z coords */
	float x1 = x0 - i1 + (1.0/6.0);
	float y1 = y0 - j1 + (1.0/6.0);
	float z1 = z0 - k1 + (1.0/6.0);
	float x2 = x0 - i2 + 2.0*(1.0/6.0);
	float y2 = y0 - j2 + 2.0*(1.0/6.0);
	float z2 = z0 - k2 + 2.0*(1.0/6.0);
	float x3 = x0 - 1.0 + 3.0*(1.0/6.0);
	float y3 = y0 - 1.0 + 3.0*(1.0/6.0);
	float z3 = z0 - 1.0 + 3.0*(1.0/6.0);

	int ii = i % 256;
	int jj = j % 256;
	int kk = k % 256;

	/* Calculate gradient incides */
	int gi0 = perm[ii+perm[jj+perm[kk]]] % 12;
	int gi1 = perm[ii+i1+perm[jj+j1+perm[kk+k1]]] % 12;
	int gi2 = perm[ii+i2+perm[jj+j2+perm[kk+k2]]] % 12;
	int gi3 = perm[ii+1+perm[jj+1+perm[kk+1]]] % 12;

	/* Calculate contributions */
	float n0, n1, n2, n3;

	float t0 = 0.6 - x0*x0 - y0*y0 - z0*z0;
	if (t0 < 0) n0 = 0.0;
	else {
		t0 *= t0;
		n0 = t0 * t0 * dot3(grad3[gi0], x0, y0, z0);
	}

	float t1 = 0.6 - x1*x1 - y1*y1 - z1*z1;
	if (t1 < 0) n1 = 0.0;
	else {
		t1 *= t1;
		n1 = t1 * t1 * dot3(grad3[gi1], x1, y1, z1);
	}

	float t2 = 0.6 - x2*x2 - y2*y2 - z2*z2;
	if (t2 < 0) n2 = 0.0;
	else {
		t2 *= t2;
		n2 = t2 * t2 * dot3(grad3[gi2], x2, y2, z2);
	}

	float t3 = 0.6 - x3*x3 - y3*y3 - z3*z3;
	if (t3 < 0) n3 = 0.0;
	else {
		t3 *= t3;
		n3 = t3 * t3 * dot3(grad3[gi3], x3, y3, z3);
	}

	/* Return scaled sum of contributions */
	return 32.0*(n0 + n1 + n2 + n3);
}
